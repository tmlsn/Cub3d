#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cpt;
	char	*cast;

	cpt = 0;
	cast = s;
	while (cpt < n)
	{
		cast[cpt] = '\0';
		cpt++;
	}
}
